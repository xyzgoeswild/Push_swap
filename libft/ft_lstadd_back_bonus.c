#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*x;

	if (!lst)
		return ;
	if ((*lst))
	{
		x = ft_lstlast(*lst);
		x->next = new;
	}
	else
		*lst = new;
}

#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	index;

	index = 0;
	if (size != 0)
	{
		while (*(src + index) && --size)
			*(dest++) = *(src + index++);
		*dest = '\0';
	}
	while (*(src + index))
		index++;
	return (index);
}
